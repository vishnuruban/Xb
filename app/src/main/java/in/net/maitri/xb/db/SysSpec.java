package in.net.maitri.xb.db;

public class SysSpec {

    private String sysKey;
    private String sysValue;
    private String sysCategory;
    private String sysComment;

    public SysSpec(){}

    public String getSysKey() {
        return sysKey;
    }

    public void setSysKey(String sysKey) {
        this.sysKey = sysKey;
    }

    public String getSysValue() {
        return sysValue;
    }

    public void setSysValue(String sysValue) {
        this.sysValue = sysValue;
    }

    public String getSysCategory() {
        return sysCategory;
    }

    public void setSysCategory(String sysCategory) {
        this.sysCategory = sysCategory;
    }

    public String getSysComment() {
        return sysComment;
    }

    public void setSysComment(String sysComment) {
        this.sysComment = sysComment;
    }
}
